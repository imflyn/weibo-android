package net.weibo.constant;

import java.io.Serializable;
import java.util.HashMap;

public class Task implements Serializable
{
    /**
     * 待执行任务类
     */
    private static final long       serialVersionUID = 1L;
    private int                     id;
    private int                     paramInt;
    private String                  paramStr;
    private boolean                 paramBool;
    private int[]                   paramInts;
    private HashMap<String, Object> params;

    public Task()
    {
        id = 0;
        paramInt = 0;
        paramStr = "";
        paramBool = false;
        params = null;
        paramInts = new int[] { 0, 0 };
    }

    public final int getId()
    {
        return id;
    }

    public final void setId(int id)
    {
        this.id = id;
    }

    public final int getParamInt()
    {
        return paramInt;
    }

    public final void setParamInt(int paramInt)
    {
        this.paramInt = paramInt;
    }

    public final String getParamStr()
    {
        return paramStr;
    }

    public final void setParamStr(String paramStr)
    {
        this.paramStr = paramStr;
    }

    public final boolean isParamBool()
    {
        return paramBool;
    }

    public final void setParamBool(boolean paramBool)
    {
        this.paramBool = paramBool;
    }

    public final int[] getParamInts()
    {
        return paramInts;
    }

    public final void setParamInts(int[] paramInts)
    {
        this.paramInts = paramInts;
    }

    public final HashMap<String, Object> getParams()
    {
        return params;
    }

    public final void setParams(HashMap<String, Object> params)
    {
        this.params = params;
    }

}
