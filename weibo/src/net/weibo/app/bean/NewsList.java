package net.weibo.app.bean;

import java.io.Serializable;
import java.util.ArrayList;

public class NewsList implements Serializable
{
    private int             timestamp;
    private int             hasnext;
    private int             totalnum = 0;
    private ArrayList<News> info;

    public final int getTimestamp()
    {
        return timestamp;
    }

    public final void setTimestamp(int timestamp)
    {
        this.timestamp = timestamp;
    }

    public final int getHasnext()
    {
        return hasnext;
    }

    public final void setHasnext(int hasnext)
    {
        this.hasnext = hasnext;
    }

    public final ArrayList<News> getInfo()
    {
        return info;
    }

    public final void setInfo(ArrayList<News> info)
    {
        this.info = info;
    }

    public final int getTotalnum()
    {
        return totalnum;
    }

    public final void setTotalnum(int totalnum)
    {
        this.totalnum = totalnum;
    }

}
