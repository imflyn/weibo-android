package net.weibo.app.bean;

import java.util.ArrayList;

public class AlbumList extends Entity
{
    private String           pagetime;
    private boolean          hasNext;
    private ArrayList<Album> albums;

    public final String getPagetime()
    {
        return pagetime;
    }

    public final void setPagetime(String pagetime)
    {
        this.pagetime = pagetime;
    }

    public final boolean isHasNext()
    {
        return hasNext;
    }

    public final void setHasNext(boolean hasNext)
    {
        this.hasNext = hasNext;
    }

    public final ArrayList<Album> getAlbums()
    {
        return albums;
    }

    public final void setAlbums(ArrayList<Album> albums)
    {
        this.albums = albums;
    }

}
